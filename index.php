			                                <a data-toggle="collapse" data-parent="#accordionEx" href="#collapseOne2" aria-expanded="false" aria-controls="collapseOne2" class="accordion-button collapsed">
			                                    How does escrow work?
			                                </a>
			                                
			                                <div id="collapseOne2" class="collapse" role="tabpanel" aria-labelledby="headingOne2" data-parent="#accordionEx">
			                                    <div class="accordion-desc">
Escrow is based on trust. An idea that one has to trust someone [or something]. In most classical terms, a Bob wants to buy some physical or digital item. He finds a seller Tom and tenders the deal. He could pay in for goods directly to Tom, but how can he be sure Tom will actually send what was agreed. Bob and Tom can agree to use Trusted Third Party [Escrow]. In that case Tom would send his goods to Escrow provider who would validate they are meeting description, and Bob could complete his part of the contract to pay for goods once escrow provider confirmed they were received.  After both parties fulfil their part of contract, escrow would send goods and funds to expected parties and that would complete the deal. If however Tom sent goods but Bob never paid or Bob paid but Tom did not send his items the escrow would revert the items to their original owners and terminate the deal. That way Bob and Tom could exchange value with confidence without implicitly trusting each other.
<br/><br/>
In the context of crypto, logistics and verifications are fairly simple, and provide opportunities for automated escrow flows. In physical world of precious metals some of these responsibilities may require specialist assessments. 
<br/><br/>
The application at this stage does not offer escrow support flows but trusted parties could be engaged outside of realms of the app at this stage. In future versions of app, the exact design is being considered and community could help to make this in a most convenient way.
<br/><br/>
However the principle of trust that should be questioned in first place, what makes one trust another party?<br/> <br/>
1) The economic incentives, the value of trust vs value of crime. <br/>
2) Historic track record, although it's never a guarantee of fairness.<br/><br/>
Hence even in anonymous marketplace it's possible to build trusting relationships between buyers and sellers based on these without trusting a third party. For example economically equivalent deposit to a decentralised network. 
Or even better permanent reputation records. For example one could burn x amount of value through coinburn mechanics to prove his long term intention to conduct business. Then one can judge if risk/value of the deal is proportional to value of seller brand. A failure to fulfil obligations would leave a permanent record on his account and diminish the value.
